y = 3

x = x + y
print(x)  # Output: 5

x = 2
x += y
print(x)  # Output: 5

x %= y # same as x = x % y
print(x)  # Output: 2

x **= y # same as x = x ** y
print(x)  # Output: 8

x = 2
# & -> and
# | -> or
# ^ -> xor
x &= y  # same as x = x & y
print(x)  # Output: 2

x = 2
x |= y  # same as x = x | y
print(x)  # Output: 3   
x = 2
x ^= y  # same as x = x ^ y
print(x)  # Output: 1       
x = 2
x <<= y  # same as x = x << y
print(x)  # Output: 16
x = 2
x >>= y  # same as x = x >> y
print(x)  # Output: 0       
x = 2
x //= y  # same as x = x // y
print(x)  # Output: 0   
